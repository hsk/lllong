#include <stdio.h>

void println(char* str) {
  printf("%s\n", str);
}

void print_b(char c) {
  printf("%d\n", c);
}

void print_s(short i) {
  printf("%d\n", i);
}

void print_i(int i) {
  printf("%d\n", i);
}

void print_l(long l) {
  printf("%ld\n", l);
}

void print_f(float f) {
  printf("%f\n", f);
}

void print_d(double d) {
  printf("%lf\n", d);
}
